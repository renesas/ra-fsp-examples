/**********************************************************************************************************************
 * Function Name: switch_ioport_cfg
 * Description  : Switches between current pin configuration (g_ioport_ctrl) and the new selected pin configuration
 * Arguments    : p_api_ctrl - i2c_stack registered in FSP
 * Return Value : NONE
 *********************************************************************************************************************/
void switch_ioport_cfg (ioport_cfg_t const * p_cfg)
{
    if (NULL != p_cfg)
    {
        R_IOPORT_Close(&g_ioport_ctrl);
        R_IOPORT_Open(&g_ioport_ctrl, p_cfg);
    }
}
