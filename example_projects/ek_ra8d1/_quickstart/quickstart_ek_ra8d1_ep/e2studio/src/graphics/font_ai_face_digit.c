/**********************************************************************************************************************
 * Function Name: rgb888torgb565
 * Description  : .
 * Argument     : rgb888Pixel
 * Return Value : .
 *********************************************************************************************************************/
static uint16_t rgb888torgb565 (uint8_t * rgb888Pixel)
{
    uint8_t red   = rgb888Pixel[2];
    uint8_t green = rgb888Pixel[1];
    uint8_t blue  = rgb888Pixel[0];

    uint16_t b = (blue >> 3) & 0x1f;
    uint16_t g = ((green >> 2) & 0x3f) << 5;
    uint16_t r = (uint16_t) ((red >> 3) & 0x1f) << 11;

    return (uint16_t) (r | g | b);
}

