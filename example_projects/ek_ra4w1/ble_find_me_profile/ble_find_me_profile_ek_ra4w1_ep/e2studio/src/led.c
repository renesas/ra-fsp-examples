extern bsp_leds_t g_bsp_leds;

void LED_LED1OnOff(bsp_io_level_t led_level)
{
    bsp_leds_t leds = g_bsp_leds;

    g_ioport.p_api->pinWrite(g_ioport.p_ctrl, (bsp_io_port_pin_t)leds.p_leds[1], led_level );
}


